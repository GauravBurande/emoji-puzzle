export const allPuzzles = [...moviePuzzles, ...songPuzzles];

export const getRandomPuzzle = (mode: 'movie' | 'song' | 'mixed', excludeAnswers: string[] = []): EmojiPuzzle => {
  let availablePuzzles: EmojiPuzzle[];
  
  switch (mode) {
    case 'movie':
      availablePuzzles = moviePuzzles;
      break;
    case 'song':
      availablePuzzles = songPuzzles;
      break;
    case 'mixed':
    default:
      availablePuzzles = allPuzzles;
      break;
  }
  
  const filteredPuzzles = availablePuzzles.filter(puzzle => !excludeAnswers.includes(puzzle.answer));
  
  if (filteredPuzzles.length === 0) {
    return availablePuzzles[Math.floor(Math.random() * availablePuzzles.length)];
  }
  
  return filteredPuzzles[Math.floor(Math.random() * filteredPuzzles.length)];
};
